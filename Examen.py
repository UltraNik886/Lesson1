p = input('Выберите Цельсия или Форингейт: ')
if p == "c":
    f = int(input('Введите градусы Форингейт: '))
    c = ((f - 32) * 5/9)
    print({f}, 'Градусов Форингейта равно', {c}, 'градусам Цельсии')
if p == "f":
    c = int(input('Введите градусы Цельсия: '))
    f = ((c * 9) / 5) + 32
    print({c}, 'Градусов Цельсия равно', {f}, 'градусам Фаренгейта')
